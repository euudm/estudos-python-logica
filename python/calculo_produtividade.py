# Definindo o prazo total
prazo_total = 24

# Calculando entregas de cada time
entregas_time_a = prazo_total / 4
entregas_time_b = prazo_total / 6
entregas_time_c = prazo_total / 8

# Somando o total
total_projetos = entregas_time_a + entregas_time_b + entregas_time_c

print(total_projetos)

#objetivo é saber quantos projetos cada time entragria juntos em 24 dias, sabendo que o time A entrega um projeto a cada 4 dias, o time B entrega um projeto a cada 6 dias e o time C entrega um projeto a cada 8 dias.
